
#include <stdio.h>
#include <string.h>
int main(){
    char a [100];
    int i, count = 0 ;

    scanf("%s", &a[i]);
    int n = strlen[a];
    

    for (i = 0; i < n; i++){
    if (a[i] == 'a'|| a[i] == 'e'|| a[i] == 'i' || a[i] == 'o' || a[i] == 'u' || a[i] == 'A'|| a[i] == 'E'|| a[i] == 'I' || a[i] == 'O' || a[i] == 'U' )
    {
        count++;
    }


    }

    printf("%d\n", count);

    return 0;
}









